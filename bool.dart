void main() {
  // tipos de dados booleanos
  bool status = true; // ou false
  print(status); // Saída: true

  // Comparação
  bool isTrue = 1 > 0; // Saída: true
  print(isTrue); // Saída: true

  // Operadores lógicos 
  String resultado; // Saída: "Verdadeiro" ou "Falso"
  resultado = status ? "Verdadeiro" : "Falso"; // Saída: "Verdadeiro"
  print(resultado); // Saída: "Verdadeiro"
}
